#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*p;
	int		i;

	if (!n)
		return (s);
	p = (char *)s;
	i = 0;
	while (n-- > 0)
		p[i++] = c;
	return (s);
}
